package Inicios;

public class Variables {
    public static void main(String[] args) {
        int edad = 35; //Declaracion e inicialización de variable en una única línea
        System.out.println("La edad uno es:"+ edad); //Concanetación de un mensaje y la impresion del valor de la variable
        edad=75; // Inicializar una varibale
        System.out.println("La edad dos es:"+ edad);// (System.out) es la función para impresion en la consola
    }
}
